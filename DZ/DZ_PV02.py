# Инициализируем счетчик
count = 0

# Проходим по всем числам от 1 до 2024
for number in range(1, 2025):
    # Преобразуем число в строку и проверяем, есть ли в нем цифра '3'
    if '3' in str(number):
        count += 1

# Выводим результат
print(f"Количество чисел от 1 до 2024, содержащих хотя бы одну цифру 3: {count}")


#Группирует одинаковые буквы и считает их количество
def compress_string(s):
    # Если строка пустая, возвращаем пустую строку
    if not s:
        return ""

    # Инициализируем результат и счетчик
    compressed = []
    count = 1  # Счетчик для текущего символа

    # Проходим по строке, начиная со второго символа
    for i in range(1, len(s)):
        # Если текущий символ совпадает с предыдущим
        if s[i] == s[i - 1]:
            count += 1  # Увеличиваем счетчик
        else:
            # Добавляем символ и его количество в сжатую строку
            compressed.append(s[i - 1] + str(count))
            count = 1  # Сбрасываем счетчик

    # Добавляем последний символ и его количество
    compressed.append(s[-1] + str(count))

    # Возвращаем сжатую строку, объединив все части
    return "".join(compressed)


# Пример использования
input_string = input ("Введи последовательно n-е кол-во символов для их сжатия: \n")
compressed_string = compress_string(input_string)
print(f"Сжатая строка: {compressed_string}")